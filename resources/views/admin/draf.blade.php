<script>
  document.addEventListener('DOMContentLoaded', () => {
  const calendarTitle = document.getElementById('calendar-title');
  const calendarDays = document.getElementById('calendar-days');
  const prevMonthBtn = document.getElementById('prev-month');
  const nextMonthBtn = document.getElementById('next-month');

  let currentDate = new Date();

  function renderCalendar(date) {
    calendarDays.innerHTML = '';

    const monthNames = [
      'January', 'February', 'March', 'April', 'May', 'June',
      'July', 'August', 'September', 'October', 'November', 'December'
    ];
    
    const year = date.getFullYear();
    const month = date.getMonth();
    const today = new Date();
    const isCurrentMonth = today.getFullYear() === year && today.getMonth() === month;

    calendarTitle.textContent = `${monthNames[month]} ${year}`;

    const firstDayOfMonth = new Date(year, month, 1).getDay();
    const lastDateOfMonth = new Date(year, month + 1, 0).getDate();
    const lastDayOfPrevMonth = new Date(year, month, 0).getDate();

   
    for (let i = firstDayOfMonth; i > 0; i--) {
      calendarDays.innerHTML += `<div class="flex p-3.5 bg-gray-50 border-r border-b border-indigo-200 text-xs font-semibold text-gray-400">${lastDayOfPrevMonth - i + 1}</div>`;
    }

    // Add current month's days
    for (let day = 1; day <= lastDateOfMonth; day++) {
        const isToday = isCurrentMonth && day === today.getDate();
        const dayClass = isToday ? "bg-indigo-600 text-white font-bold rounded-full" : "bg-white text-gray-900 cursor-pointer hover:bg-indigo-50 transition-all duration-300";

        calendarDays.innerHTML += `<div class="flex p-3.5 border-r border-b border-indigo-200 text-xs font-semibold ${dayClass} justify-center">${day}</div>`;
    }

    // Add next month's leading days to fill the grid
    const totalCells = firstDayOfMonth + lastDateOfMonth;
    const nextDays = totalCells <= 35 ? 35 - totalCells : 42 - totalCells;

    for (let i = 1; i <= nextDays; i++) {
      calendarDays.innerHTML += `<div class="flex p-3.5 bg-gray-50 border-r border-b border-indigo-200 text-xs font-semibold text-gray-400">${i}</div>`;
    }
}


  prevMonthBtn.addEventListener('click', () => {
    currentDate.setMonth(currentDate.getMonth() - 1);
    renderCalendar(currentDate);
  });

  nextMonthBtn.addEventListener('click', () => {
    currentDate.setMonth(currentDate.getMonth() + 1);
    renderCalendar(currentDate);
  });

  renderCalendar(currentDate); // Initial render
});

  </script>